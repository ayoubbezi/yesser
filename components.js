// Get current language from localStorage or default to English
let currentLang = localStorage.getItem('language') || 'en';

// Set document language attribute
document.documentElement.lang = currentLang;

// Function to get translation for a key
function t(key) {
  return translations[currentLang][key] || key;
}

// Function to change language
function changeLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('language', lang);
  
  // Set document language attribute
  document.documentElement.lang = lang;
  
  // Apply RTL direction for Arabic
  if (lang === 'ar') {
    document.documentElement.dir = 'rtl';
    document.body.classList.add('rtl');
  } else {
    document.documentElement.dir = 'ltr';
    document.body.classList.remove('rtl');
  }
  
  // Translate the current page without reload
  translatePage();
}

// Function to translate the current page
function translatePage() {
  // Translate all elements with data-translate attribute
  document.querySelectorAll('[data-translate]').forEach(element => {
    const key = element.getAttribute('data-translate');
    if (translations[currentLang][key]) {
      element.textContent = translations[currentLang][key];
    }
  });
  
  // Translate placeholders
  document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
    const key = element.getAttribute('data-translate-placeholder');
    if (translations[currentLang][key]) {
      element.placeholder = translations[currentLang][key];
    }
  });
  
  // Translate buttons
  document.querySelectorAll('.add-cart-btn').forEach(button => {
    button.textContent = t('addToCart');
  });
  
  // Translate section titles
  const sectionTitle = document.querySelector('.section-title');
  if (sectionTitle && sectionTitle.textContent.includes('Our Products')) {
    sectionTitle.textContent = t('ourProducts');
  }
  
  // Translate banner content
  const bannerSubtitle = document.querySelector('.banner-subtitle');
  if (bannerSubtitle) bannerSubtitle.textContent = t('trendingItem');
  
  const bannerTitle = document.querySelector('.banner-title');
  if (bannerTitle) bannerTitle.textContent = t('summerCollection');
  
  const bannerText = document.querySelector('.banner-text');
  if (bannerText) bannerText.textContent = t('startingAt');
  
  const bannerBtn = document.querySelector('.banner-btn');
  if (bannerBtn) bannerBtn.textContent = t('shopNow');
  
  // Translate contact section
  const contactTitle = document.querySelector('#contact .section-title');
  if (contactTitle) contactTitle.textContent = t('contactHeader');
  
  const contactSubtitle = document.querySelector('#contact .section-subtitle');
  if (contactSubtitle) contactSubtitle.textContent = t('contactSubtitle');
  
  // Update search field placeholder
  const searchField = document.querySelector('.search-field');
  if (searchField) searchField.placeholder = t('searchPlaceholder');
  
  // Force reload header and footer to apply translations
  const headerContainer = document.getElementById('header-container');
  if (headerContainer) headerContainer.innerHTML = loadHeader();
  
  const footerContainer = document.getElementById('footer-container');
  if (footerContainer) footerContainer.innerHTML = loadFooter();
  
  // Re-initialize any components that need it after translation
  if (typeof initializeComponents === 'function') {
    initializeComponents();
  }
}

