// Function to initialize search
function initializeSearch() {
  const searchField = document.querySelector('.search-field');
  if (!searchField) return;
  
  // Add the search dropdown styles to the document
  addSearchDropdownStyles();
  
  // Create search results dropdown if it doesn't exist
  let searchResultsDropdown = document.querySelector('.search-results-dropdown');
  if (!searchResultsDropdown) {
    searchResultsDropdown = document.createElement('div');
    searchResultsDropdown.className = 'search-results-dropdown';
    const searchContainer = document.querySelector('.search-container');
    if (searchContainer) {
      searchContainer.appendChild(searchResultsDropdown);
    } else {
      searchField.parentNode.appendChild(searchResultsDropdown);
    }
  }
  
  // Set up event listeners for the search field
  let debounceTimer;
  
  searchField.addEventListener('input', function() {
    const query = this.value.trim();
    
    // Clear previous timer
    clearTimeout(debounceTimer);
    
    // Hide dropdown if query is too short
    if (query.length < 2) {
      searchResultsDropdown.style.display = 'none';
      return;
    }
    
    // Debounce the search to avoid too many requests
    debounceTimer = setTimeout(() => {
      performLiveSearch(query);
    }, 250); // 250ms debounce time
  });
  
  // Add focus event to show results when field is focused
  searchField.addEventListener('focus', function() {
    const query = this.value.trim();
    if (query.length >= 2) {
      performLiveSearch(query);
    }
  });
  
  // Close dropdown when clicking outside
  document.addEventListener('click', function(e) {
    if (!searchField.contains(e.target) && !searchResultsDropdown.contains(e.target)) {
      searchResultsDropdown.style.display = 'none';
    }
  });
  
  // For mobile: Add touch event handlers to improve touch experience
  const isMobile = window.innerWidth <= 767;
  if (isMobile) {
    // Auto-focus input field when clicking search icon (common on mobile UIs)
    const searchBtn = document.querySelector('.search-btn');
    if (searchBtn) {
      searchBtn.addEventListener('click', function(e) {
        if (searchField.value.trim().length === 0) {
          e.preventDefault(); // Prevent form submission if empty
          searchField.focus();
        }
      });
    }
    
    // Add close dropdown on scroll (since mobile users often scroll to dismiss)
    window.addEventListener('scroll', function() {
      if (searchResultsDropdown.style.display === 'block') {
        // Only close if scrolled more than a small threshold
        if (window.scrollY > 10) {
          searchResultsDropdown.style.display = 'none';
        }
      }
    });
    
    // Close dropdown when switching to another app or tab (mobile behavior)
    document.addEventListener('visibilitychange', function() {
      if (document.visibilityState !== 'visible') {
        searchResultsDropdown.style.display = 'none';
      }
    });
  }
}

// Function to perform live search - Move this outside initializeSearch function to make it globally available
async function performLiveSearch(query) {
  try {
    // Fetch products from API
    const response = await fetch(`${API_BASE_URL}/api/products`);
    if (!response.ok) throw new Error('Failed to fetch products');
    
    const products = await response.json();
    
    // Filter products based on query
    const filteredProducts = products.filter(product => {
      const name = product.title || product.name || '';
      const description = product.description || '';
      
      return name.toLowerCase().includes(query.toLowerCase()) || 
             description.toLowerCase().includes(query.toLowerCase());
    }).slice(0, 6); // Limit to 6 results
    
    // Display the results
    displayLiveSearchResults(filteredProducts, query);
    
  } catch (error) {
    console.error('Search error:', error);
    const searchResultsDropdown = document.querySelector('.search-results-dropdown');
    if (searchResultsDropdown) {
      searchResultsDropdown.innerHTML = `
        <div class="search-dropdown-header">
          <h3 class="search-title">Search Results</h3>
          <button class="close-search-btn" onclick="closeSearchDropdown()">
            <ion-icon name="close-outline"></ion-icon>
          </button>
        </div>
        <div class="no-results">An error occurred while searching</div>
      `;
      searchResultsDropdown.style.display = 'block';
    }
  }
}

// Function to display live search results in dropdown
function displayLiveSearchResults(results, query) {
  const searchResultsDropdown = document.querySelector('.search-results-dropdown');
  
  // Create dropdown if it doesn't exist
  if (!searchResultsDropdown) {
    const newDropdown = document.createElement('div');
    newDropdown.className = 'search-results-dropdown';
    const searchContainer = document.querySelector('.search-container');
    if (searchContainer) {
      searchContainer.appendChild(newDropdown);
    } else {
      document.querySelector('.search-field').parentNode.appendChild(newDropdown);
    }
  }
  
  const dropdown = document.querySelector('.search-results-dropdown');
  
  if (results.length === 0) {
    dropdown.innerHTML = `
      <div class="search-dropdown-header">
        <h3 class="search-title">Search Results</h3>
        <button class="close-search-btn" onclick="closeSearchDropdown()">
          <ion-icon name="close-outline"></ion-icon>
        </button>
      </div>
      <div class="no-results">No products found</div>
    `;
    dropdown.style.display = 'block';
    return;
  }

  console.log('Search results:', results);
  
  // Build results HTML
  let resultsHTML = `
    <div class="search-dropdown-header">
      <h3 class="search-title">Search Results</h3>
      <button class="close-search-btn" onclick="closeSearchDropdown()">
        <ion-icon name="close-outline"></ion-icon>
      </button>
    </div>
  `;
  
  results.forEach(product => {
    // Process image URL
    let imagePath = product.images && product.images.length > 0 ? product.images[0] : 
                   product.image ? product.image : './assets/images/products/default.jpg';
                   
    // Format image path using our helper function
    imagePath = getImageUrl(imagePath);
    
    // Get product name (some products use title, others use name)
    const productName = product.title || product.name || 'Product';
    
    // Format price
    const price = product.price ? `$${parseFloat(product.price).toFixed(2)}` : '';
    
    // Get product ID (handle both _id and id formats)
    const productId = product._id || product.id;
    console.log(`Product in search: ${productName}, ID: ${productId}, _id: ${product._id}, id: ${product.id}`);
    
    // Create result item HTML
    resultsHTML += `
      <div class="search-result-item" onclick="navigateToProduct('${productId}')">
        <div class="search-result-image">
          <img src="${imagePath}" alt="${productName}" onerror="this.src='./images/placeholder.jpg'">
        </div>
        <div class="search-result-info">
          <div class="search-result-title">${productName}</div>
          <div class="search-result-price">${price}</div>
        </div>
      </div>
    `;
  });
  
  // Add "View all results" link
  resultsHTML += `
    <div class="view-all-results">
      <a href="search-results.html?search=${encodeURIComponent(query)}">View all results</a>
    </div>
  `;
  
  // Update dropdown content and show it
  dropdown.innerHTML = resultsHTML;
  dropdown.style.display = 'block';
}

